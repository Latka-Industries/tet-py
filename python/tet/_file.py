def _parse_json_response(raw: str) -> dict[str, Any]:
    return cast(dict[str, Any], json.loads(raw))


def _resolve_path(path: str | PathLike[str]) -> Path:
    """Expand `~` and normalize before passing paths to Rust."""
    return Path(path).expanduser()


class TetFile:
    """Read-only handle to one ``.tet`` file (catalog + query engine).

    Use :meth:`mean`, :meth:`sum`, etc. for reductions, :meth:`execute` for generic
    documents, and :meth:`dataset` / :meth:`iter_datasets` for catalog metadata.
    """

    def __init__(self, inner: _native.TetFile) -> None:
        self._inner = inner
        self._summary_cache: dict[str, Any] | None = None  # parsed summary_json()

    @classmethod
    def open(cls, path: str | PathLike[str]) -> TetFile:
        """Open a ``.tet`` file read-only via mmap.

        Parameters
        ----------
        path : str or path-like
            Filesystem path to the ``.tet`` file. ``~`` is expanded.

        Returns
        -------
        TetFile
            Handle for catalog reads and queries.

        Raises
        ------
        OSError
            If the path does not exist or cannot be opened.
        CatalogError
            If the file layout or catalog cannot be read.
        """
        return cls(_native.open(_resolve_path(path)))

    def __enter__(self) -> TetFile:
        return self

    def __exit__(self, *args: object) -> None:
        return None

    def __iter__(self) -> Iterator[str]:
        """Iterate dataset names (same order as the catalog)."""
        yield from self.datasets()

    def __getattr__(self, name: str) -> Any:
        # Forward datasets(), summary_json(), etc. from tet._native.TetFile.
        return getattr(self._inner, name)

    @property
    def path(self) -> Path:
        """Resolved path to the open ``.tet`` file."""
        return Path(self._inner.path)

    @property
    def dataset_names(self) -> tuple[str, ...]:
        """Dataset names in this file (catalog order)."""
        return tuple(self.datasets())

    def typing_stub(self) -> str:
        """Return ``.pyi`` text with ``Literal`` dataset (and axis) names for this file.

        Returns
        -------
        str
            Python source for optional IDE stubs. Save beside your code (e.g.
            ``mydata_tet.pyi``); not written automatically on :func:`tet.open`.
        """
        lines = [
            "# Autogenerated from tet.TetFile.typing_stub() â€” names are per .tet file.",
            f"# source: {self.path}",
            "from __future__ import annotations",
            "from typing import Literal",
            "",
        ]
        names = self.dataset_names
        if names:
            inner = ", ".join(repr(n) for n in names)
            lines.append(f"DatasetName = Literal[{inner}]")
            lines.append("")
        for ds in self.iter_datasets():
            if ds.dim_names:
                tag = ds.name.replace("-", "_").replace(" ", "_")
                inner = ", ".join(repr(n) for n in ds.dim_names)
                lines.append(
                    f"AxisName_{tag} = Literal[{inner}]  # dim_names for {ds.name!r}"
                )
        if len(lines) == 5:
            lines.append("# (no dim_names metadata in footer for any dataset)")
        return "\n".join(lines) + "\n"

    def _summary_dict(self) -> dict[str, Any]:
        """Cached ``TetFileSummaryV1`` dict from Rust (same as :meth:`summary`)."""
        if self._summary_cache is None:
            self._summary_cache = _parse_json_response(self._inner.summary_json())
        return self._summary_cache

    def _metadata_for_dataset(self, name: str) -> dict[str, Any]:
        meta = self._summary_dict().get("metadata") or {}
        datasets = meta.get("datasets") or {}
        entry = datasets.get(name)
        return entry if isinstance(entry, dict) else {}

    def iter_datasets(self) -> Iterator[Dataset]:
        """Iterate [`Dataset`] records (name, shape, optional dim_names)."""
        for record in self._summary_dict()["datasets"]:
            name = str(record["name"])
            dim_names = self._metadata_for_dataset(name).get("dim_names")
            yield dataset_from_summary(record, dim_names)

    def _require_dataset(self, name: str) -> Dataset:
        """Resolve ``name`` or raise :class:`~tet.UnknownDatasetError` before query."""
        for ds in self.iter_datasets():
            if ds.name == name:
                return ds
        raise UnknownDatasetError(name, path=self.path, available=self.dataset_names)

    def dataset(self, key: str | int) -> Dataset:
        """Look up one dataset by catalog index or name.

        Parameters
        ----------
        key : str or int
            Dataset name, or catalog index ``0``, ``1``, â€¦ (same order as :meth:`iter_datasets`).

        Returns
        -------
        Dataset
            Shape, dtype tag, chunk_shape, and optional ``dim_names``.

        Raises
        ------
        UnknownDatasetError
            If ``key`` is a string name not in this file.
        IndexError
            If ``key`` is an integer index out of range.
        """
        if isinstance(key, int):
            datasets = list(self.iter_datasets())
            try:
                return datasets[key]
            except IndexError as exc:
                names = self.dataset_names
                raise IndexError(
                    f"dataset index {key} out of range for {self.path} "
                    f"(count={len(datasets)}; names: {', '.join(repr(n) for n in names)})"
                ) from exc
        return self._require_dataset(key)

    def __getitem__(self, key: str | int) -> Dataset:
        """``f[0]`` or ``f['temperature']`` â†’ [`Dataset`]."""
        return self.dataset(key)

    def summary(self) -> dict[str, Any]:
        """Return the full catalog summary for this file.

        Returns
        -------
        dict
            ``TetFileSummaryV1`` JSON: ``superblock``, ``datasets``, ``chunks``,
            ``file_execution``, ``history``, ``metadata``. Same as ``tet info --json``.
        """
        return dict(self._summary_dict())

    def info(self) -> dict[str, Any]:
        """Alias for :meth:`summary` (includes every chunk index row)."""
        return self.summary()

    @overload
    def query(self, query: Any, *, raw: Literal[True]) -> dict[str, Any]: ...

    @overload
    def query(self, query: Any, *, raw: Literal[False]) -> QueryResult: ...

    def query(self, query: Any, *, raw: bool = True) -> dict[str, Any] | QueryResult:
        f"""Execute a query document (``tet query -t file.tet -x``).

        Parameters
        ----------
        query : dict or str
            Query document (dict) or JSON string. Must include ``"dataset"`` and one op key
            (e.g. ``"mean": []``) unless planning only via :meth:`plan_only`.
        raw : bool, default True
            If True, return the full ``QueryResponse`` dict. If False, return :class:`~tet.QueryResult`.

        Returns
        -------
        dict or QueryResult
            Wire JSON when ``raw=True``; parsed result with ``.scalar`` / ``.reduced`` when ``raw=False``.

        Raises
        ------
        {_RAISE_FILE_QUERY}
        """
        doc = coerce_query_doc(query)
        if "dataset" in doc and isinstance(doc["dataset"], str):
            self._require_dataset(doc["dataset"])
        out = _parse_json_response(self._inner.query(doc))
        check_query_response(
            out,
            path=self.path,
            require_execution=op_key_from_doc(doc) is not None,
        )
        if raw:
            return out
        return QueryResult.from_response(
            out,
            op=op_key_from_doc(doc),
            path=str(self.path),
            require_execution=False,
        )

    @overload
    def query_execute(
        self,
        query: Any,
        *,
        device: str | None = None,
        raw: Literal[True] = True,
    ) -> dict[str, Any]: ...

    @overload
    def query_execute(
        self,
        query: Any,
        *,
        device: str | None = None,
        raw: Literal[False],
    ) -> QueryResult: ...

    def query_execute(
        self,
        query: Any,
        *,
        device: str | None = None,
        raw: bool = True,
    ) -> dict[str, Any] | QueryResult:
        f"""Like :meth:`query`, with optional ``execution.device``.

        Parameters
        ----------
        query : dict or str
            Query document (see :meth:`query`).
        device : str, optional
            Written to ``execution.device`` before execute (e.g. ``"cpu"``).
        raw : bool, default True
            Same as :meth:`query`.

        Returns
        -------
        dict or QueryResult
            Same as :meth:`query`.

        Raises
        ------
        {_RAISE_FILE_QUERY}
        """
        doc = coerce_query_doc(query)
        if device is not None:
            execution = dict(doc.get("execution") or {})
            execution["device"] = device
            doc = {**doc, "execution": execution}
        if raw:
            return self.query(doc, raw=True)
        return self.query(doc, raw=False)

    def execute(
        self,
        query: Any,
        *,
        device: str | None = None,
        raw: bool = False,
        plan: bool = False,
    ) -> dict[str, Any] | QueryResult:
        f"""Run or plan a query; default return is :class:`~tet.QueryResult` (``raw=False``).

        Parameters
        ----------
        query : dict or str
            Query document (see :meth:`query`).
        device : str, optional
            Passed to :meth:`query_execute` when executing.
        raw : bool, default False
            If True, return wire ``dict``; if False, return :class:`~tet.QueryResult`.
        plan : bool, default False
            If True, call :meth:`plan_only` (no op keys; no ``execution`` block).

        Returns
        -------
        dict or QueryResult
            Plan or execute result depending on ``plan`` and ``raw``.

        Raises
        ------
        {_RAISE_FILE_QUERY}
        """
        if plan:
            if raw:
                return self.plan_only(query, raw=True)
            return self.plan_only(query, raw=False)
        if raw:
            return self.query_execute(query, device=device, raw=True)
        return self.query_execute(query, device=device, raw=False)

    @overload
    def plan_only(self, query: Any, *, raw: Literal[True]) -> dict[str, Any]: ...

    @overload
    def plan_only(self, query: Any, *, raw: Literal[False]) -> QueryResult: ...

    def plan_only(
        self, query: Any, *, raw: bool = True
    ) -> dict[str, Any] | QueryResult:
        f"""Plan a query without executing (``tet query`` without ``-x``).

        Parameters
        ----------
        query : dict or str
            Document with ``"dataset"`` and optional ``"selection"`` only (no ``mean``/``sum``/â€¦).
        raw : bool, default True
            If True, wire ``dict``; if False, :class:`~tet.QueryResult` (typically no ``.scalar``).

        Returns
        -------
        dict or QueryResult
            Response with plan/catalog fields; ``execution`` is usually absent.

        Raises
        ------
        {_RAISE_FILE_QUERY}
        """
        doc = coerce_query_doc(query)
        if "dataset" in doc and isinstance(doc["dataset"], str):
            self._require_dataset(doc["dataset"])
        out = _parse_json_response(self._inner.plan_only(doc))
        check_query_response(out, path=self.path, require_execution=False)
        if raw:
            return out
        return QueryResult.from_response(
            out, op=None, path=str(self.path), require_execution=False
        )

    def _resolve_reduction_axes(
        self,
        dataset: str,
        axes: Sequence[int | str] | None,
        *,
        axis: int | str | None,
    ) -> list[int | str]:
        """Wire axis list for list-style ops; ``[]`` means reduce all dimensions."""
        if axis is not None and axes is not None:
            raise TypeError("pass only one of axes= or axis=")
        merged = axes_for_query(axes if axis is None else [axis])
        if not merged:
            return []
        self._require_dataset(dataset)
        return axes_wire(self.dataset(dataset), merged, path=self.path)

    def _execute_doc(
        self,
        doc: dict[str, Any],
        *,
        device: str | None = None,
        raw: bool = False,
        scalar_op: str | None = None,
    ) -> float | list[float] | dict[str, Any] | QueryResult:
        """Run a built document; unwrap scalars when ``scalar_op`` is set."""
        if raw:
            if device is not None:
                return self.query_execute(doc, device=device, raw=True)
            return self.query(doc, raw=True)
        result = self.query_execute(doc, device=device, raw=False)
        assert isinstance(result, QueryResult)
        if scalar_op and result.scalar is not None:
            return result.scalar
        if scalar_op in ("histogram", "covariance", "correlation", "transform"):
            return result
        if scalar_op and result.reduced is not None:
            return result
        return result

    def _reduce(
        self,
        op: str,
        dataset: str,
        axes: Sequence[int | str] | None = None,
        *,
        axis: int | str | None = None,
        device: str | None = None,
        raw: bool = False,
    ) -> float | int | bool | dict[str, Any] | QueryResult:
        """Shared path for ``mean`` / ``sum`` / ``min`` / â€¦ list-style reductions."""
        wire_axes = self._resolve_reduction_axes(dataset, axes, axis=axis)
        doc = reduction_doc(dataset, op, wire_axes)
        if raw:
            if device is not None:
                return self.query_execute(doc, device=device, raw=True)
            return self.query(doc, raw=True)
        result = self.query_execute(doc, device=device, raw=False)
        assert isinstance(result, QueryResult)
        if wire_axes:
            return result
        if result.scalar is None:
            raise TetError(f"missing scalar for op {op!r}")
        return result.scalar

